package com.company;

public class kk_22_do_while_loop {
    public static void main(String[] args) {
//       Do While loop use & condition
        System.out.println("Do While loop");
        int i=0;
        do {
            System.out.println("The value of i is ::"+i);
            i++;
        }while (i<5);

    }
}
